package com.bilqismufida.uasganjilpraktekkotlin

fun main() {
    // String
    val string : String = "Ujian Akhir Semester Kotlin"
    println(string)

    // Float
    val float : Float = 17.23F
    println(float)

    // Double
    val double : Double = 8.2783682378

    // Byte
    val byte : Byte = 50
    println(byte)

    // Short
    val short : Short = 2021
    println(short)

    // Long
    val long : Long = 7269920029
    println(long)

    // Boolean
    val boolean : Boolean = false
    println(boolean)

    // Character
    val char : Char = 'O'
}